# >
print(2 > 1)  # True
print(5 > 10)  # False
print(10 > 10)  # False

# <
print(1 < 2)  # True
print(10 < 5)  # False
print(10 < 10)  # False

# >=
print(2 >= 1)  # True
print(10 >= 10)  # True
print(10 >= 15)  # False

# <=
print(1 <= 2)  # True
print(10 <= 10)  # True
print(15 <= 10)  # False

# ==
print(1 == 1)  # True
print(2 == 5)  # False
print(2 == '2')  # False
print('Apple' == "Apple")  # True
print('Apple' == "apple")  # False

# !=
print(1 != 1)  # False
print(2 != 5)  # True
print(2 != '2')  # True
print('Apple' != "Apple")  # False
print('Apple' != "apple")  # True

